    <h1>Center of Geographic Sciences - IT Programming Year 1 & 2 Course List and Description</h1>

    <h3>DBAS 1007</h3>
    <h4>Data Fundamentals</h4>
    <p>This course introduces students to the concept of a database management system and its role in the IT infrastructure of any business. Students will use a database management system to develop a database and use this database to store and manipulate data. 
        Students will also be asked to interpret database diagrams and create database objects based on their meaning.</p>

    <h3>ICOM 2701</h3>
    <h4>Professional Practice for IT I</h4>
    <p>This course exposes students to tools and process that support professional continuous improvement. This course builds the foundations for professional habits and methodologies that help graduates remain current and relevant in an ever-evolving profession.</p>

    <h3>ICOM 2702</h3>
    <h4>Professional Practice for IT II</h4>
    <p>This course develops the skills, attitudes, processes and habits required to successfully prepare for a variety of interview scenarios and the associated presentation techniques. 
        Emphasis is on the development of a professional portfolio and the part that personal marketing plays in gaining successful, relevant employment.</p>

    <h3>ICOM 2703</h3>
    <h4>Professional Practice for IT III</h4>
    <p>This course provides students with the skills and knowledge to curate a personal, professional portfolio in readiness for employment in the IT industry. 
        In addition, students will practice and prepare for numerous interview situations and scenarios by honing the skills and techniques previously acquired.</p>

    <h3>INET 2005</h3>
    <h4>Web Application Programming I</h4>
    <p>This course focuses on the planning, design and development of dynamic (data-aware) web pages using server-side scripting and database connectivity.</p>

    <h3>INFT 2100</h3>
    <h4>Project Management</h4>
    <p>This course covers the basic theory and skills that introduce the learners to the process of effective project management and the unique challenges of Information Technology (IT) projects and methodologies.</p>

    <h3>INFT 3000</h3>
    <h4>Capstone</h4>
    <p>Learners acquire hands-on experience on a group project applying much of the material learned throughout their program. Throughout this course, learners will apply their skills to prepare and deliver technical documentation and presentations using various media platforms.</p>

    <h3>INFT 4100</h3>
    <h4>Work Experience</h4>
    <p>This course is a summative experience in the context of a real-world organization. Students will apply learning in an authentic professional IT working environment and gain hands-on industry related skills and knowledge.</p>

    <h3>MOBI 3002</h3>
    <h4>Mobile Application Development - Android</h4>
    <p>In this course students will develop mobile software applications that run on the Android platform.</p>

    <h3>NETW 1027</h3>
    <h4>Introduction to Networking and Security</h4>
    <p>This course is focused on supporting network connected devices covering troubleshooting and maintaining connectivity from a client perspective.</p>

    <h3>OSYS 1000</h3>
    <h4>Operating Systems - Linux</h4>
    <p>This course introduces the skills and knowledge that will enable the student to install, configure, maintain and troubleshoot a current UNIX based operating system (OS) in standalone workstation mode.</p>

    <h3>OSYS 1200</h3>
    <h4>Introduction to Windows Administration</h4>
    <p>This course is an introductory course to familiarize the student with key topics in managing a Windows client operating system.</p>

    <h3>PROG 1400</h3>
    <h4>Introduction to Object Oriented Programming</h4>
    <p>This course is an introduction to the essentials of object oriented programming (OOP). This course provides an explanation of the paradigm on which OOP is based, and how it affects programming strategies and solutions.</p>

    <h3>PROG 1700</h3>
    <h4>Logic and Programming</h4>
    <p>This course introduces the fundamentals of logic and computer programming and their role in the creation of software solutions for any problem. Logic is used to break down complex problems into manageable tasks to allow for comprehensive problem-solving using computer code. 
        This course covers the process of creating logical designs and implementing them as programmatic code to build computer software programs.</p>

    <h3>PROG 2007</h3>
    <h4>Programming in C</h4>
    <p>This course introduces students to the C programming language, focusing on the various uses of pointers, efficient memory management and interfacing with the standard C libraries. 
        The course will also highlight the differences between the C language and other common programming languages that learners may be familiar with, in terms of syntax and coding practices.</p>

    <h3>PROG 2100</h3>
    <h4>Programming C++</h4>
    <p>This course introduces learners to the C++ programming language, focusing on the use of pointers, memory management, generic programming and the Standard Template Library. 
        Exploration of the language in a project context aims at reinforcing object-orient design principles; use of idioms & design patterns; use of UML in design and documentation; and utilization of third-party libraries.</p>

