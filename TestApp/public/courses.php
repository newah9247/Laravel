    <h3>PROG 2400</h3>
    <h4>Data Structures</h4>
    <p>This course presents fundamental data structures and algorithms with a pragmatic, application driven approach. Selected data structures are examined in detail together with implementation issues, and an in-depth discussion of their corresponding algorithms.

